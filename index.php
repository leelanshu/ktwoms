   <script>function calculate() {
  var investment = parseFloat(document.getElementById('investment').value);
  var duration = parseFloat(document.getElementById('duration').value);
  var rate = parseFloat(document.getElementById('rate').value);

  if (isNaN(investment) || isNaN(duration) || isNaN(rate)) {
    alert('Please enter valid inputs.');
    return;
  }

  var monthlyRate = rate / 12 / 100;
  var months = duration * 12;

  var maturityAmount = investment * (((Math.pow(1 + monthlyRate, months) - 1) / monthlyRate) * (1 + monthlyRate));

  document.getElementById('result').innerHTML = 'Maturity Amount: ₹' + maturityAmount.toFixed(2);
}


</script>
